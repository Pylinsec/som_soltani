# float and integer   ya ashari va adad sahih

# addition +
a = 67
b = 26
print(a + b) # f string  f" amal jame : {a} + {b} = {a + b}"
print(f"{a} + {b} = {a + b}")

# menha or subtraction
print(a - b)
print(f"{a} - {b} = {a - b}")

# * multiplication   ya zarb
print(a * b)
print(f"{a} * {b} = {a * b}")

# division /   taqsim
print(a / b)
print(f"{a} / {b} = {a / b}") 
print(f"{a} // {b} = {a // b}")  # floor  yani gerd kardan be samt paein

# exponentiation   tevan  **
a = 3
b = 5
print(a ** b)
print(f"{a} be tevan  {b} = {a ** b}")

# modulus  baqimande taqsim
a = 67
b = 26
print(a % b)
print(f"baqimandeh taqsim {a} bar  {b} = {a % b}")

